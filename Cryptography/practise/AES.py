def xor(row1, row2):
    ans = [row1[i] ^ row2[i] for i in range(4)]
    return ans


def shift(row, n=1):
    row = row[n:] + row[:n]
    return row


def sub_bytes(row):
    ans = []
    for i in row:
        ans.append(Sbox[i])
    return ans


def g(word, round_number):
    ans = shift(word)
    ans = sub_bytes(ans)
    ans = xor(ans, rcon[round_number])
    return ans


def setKey(key):
    key = [int(hex(ord(ch)), 16) for ch in key]
    word = [[], [], [], []]
    for i in range(0, 16, 4):
        word[i // 4] = key[i : i + 4]
    round_key = []
    round_key.append([word[0], word[1], word[2], word[3]])
    for i in range(0, 10):
        temp = g(word[3], i)
        word[0] = xor(word[0], temp)
        word[1] = xor(word[1], word[0])
        word[2] = xor(word[2], word[1])
        word[3] = xor(word[3], word[2])
        round_key.append([word[0], word[1], word[2], word[3]])
    return round_key


def transpose(matrix):
    new_matrix = [[0 for _ in range(4)] for _ in range(4)]
    for i in range(4):
        for j in range(4):
            new_matrix[i][j] = matrix[j][i]
    return new_matrix


def addRoundKey(state, key):
    new_state = []
    for i in range(4):
        temp = []
        for j in range(4):
            temp.append(state[i][j] ^ key[i][j])
        new_state.append(temp)
    return new_state


def multiply_by_2(v):
    s = v << 1
    s &= 0xFF
    if (v & 128) != 0:
        s = s ^ 0x1B
    return s


def multiply_by_3(v):
    return multiply_by_2(v) ^ v


def mixColumn(column):
    r = [
        multiply_by_2(column[0]) ^ multiply_by_3(column[1]) ^ column[2] ^ column[3],
        multiply_by_2(column[1]) ^ multiply_by_3(column[2]) ^ column[3] ^ column[0],
        multiply_by_2(column[2]) ^ multiply_by_3(column[3]) ^ column[0] ^ column[1],
        multiply_by_2(column[3]) ^ multiply_by_3(column[0]) ^ column[1] ^ column[2],
    ]
    return r


def mixColumns(grid):
    new_grid = [[], [], [], []]
    for i in range(4):
        col = [grid[j][i] for j in range(4)]
        col = mixColumn(col)
        for i in range(4):
            new_grid[i].append(col[i])
    return new_grid


